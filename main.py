import time

from ultralytics import YOLO
from ultralytics.utils import SETTINGS


def main():
    # -----------------------------------------------------------------------------
    # 0. 初始化设置
    # -----------------------------------------------------------------------------
    print("=" * 50)
    print("🚀 开始 YOLOv5 训练流程")
    print("=" * 50)

    # 加载模型配置文件（yolov5n.yaml 是 YOLOv5 的 nano 版本，最轻量）
    # 模型文件位于 ultralytics 库内部，无需手动下载
    model = YOLO("yolov5n.yaml")  # model = YOLO('runs/detect/yolov5_26_0315_1774499174.8385925/weights/last.pt')

    # 开启 Tensorboard 支持，方便在训练过程中可视化 Loss 曲线
    SETTINGS["tensorboard"] = True

    print("✅ 模型配置文件加载完成：yolov5n.yaml")
    print("🔧 正在配置训练参数...")

    time.time()  # 获取当前时间戳，用于生成唯一的实验名称

    # 训练参数设置（可以根据硬件情况和需求调整）
    print("⚙️ 训练参数设置：epochs=100, batch=2, imgsz=640 (CPU 快速测试模式)")

    # model.train() 开始训练，所有参数均以关键字形式传入
    results = model.train(
        # =========================================================================
        # 第一部分：基础环境与任务配置 (Infrastructure & Task)
        # =========================================================================
        data="../datasets_final/data.yaml",  # [必须] 数据集配置文件路径，内部定义了 train/val 图像路径和类别名称
        # 这里使用内置的 COCO128 迷你数据集（会自动下载）
        project="runs/detect",  # 项目文件夹名，所有实验都会保存在这个目录下
        name="Baseline111",  # name=f'yolov5n_26_0402_{timestep}',  # 本次实验的子文件夹名，例如: runs/detect/yolov5_26_0117_xxx
        device="0",  # 计算设备选择：
        #   "" (空) = 自动选择 GPU (如果有)
        #   "0" = 指定第一张显卡
        #   "0,1" = 多卡分布式训练
        #   "cpu" = 强制使用 CPU（当前设置）
        workers=2,  # 数据加载线程数 (DataLoader workers)
        #   Windows 系统必须设为 0，否则可能报错
        #   如果是 Linux 且有较多 CPU 核心，可以设为 4-8
        # resume=True,
        # =========================================================================
        # 第二部分：核心训练超参数 (Training Hyperparameters)
        # =========================================================================
        epochs=100,  # 训练总轮数（一个 epoch 表示遍历一次全部训练数据）
        #   200-300 是 YOLO 的标准范围
        #   这里设为 5 仅用于快速测试流程
        batch=32,  # 批次大小（一次送入多少张图片）
        #   在显存允许的情况下越大越好，但 CPU 训练需调小
        cache=False,  # 修改：关闭缓存（否则需要50GB内存，你机器不够）
        imgsz=640,  # 输入图像尺寸（必须是 32 的倍数）
        #   越大越能检测小目标，但更慢且耗内存
        amp=True,  # 自动混合精度训练（FP16）
        #   可减少显存占用、加速训练，精度几乎无损
        pretrained=True,  # 是否加载预训练权重（在 COCO 上预训练的权重）
        #   增量训练
        #   False = 从头训练（随机初始化）
        #   True = 加载官方预训练模型，加快收敛
        # =========================================================================
        # 第三部分：优化器与学习率 (Optimizer & Scheduler)
        # =========================================================================
        optimizer="AdamW",  # 优化器选择：
        #   'SGD'：YOLO 默认，泛化性好但参数敏感
        #   'AdamW'：对学习率不敏感，收敛快，适合中小数据集
        lr0=0.001,  # 初始学习率（AdamW 通常用 0.001，SGD 用 0.01）
        lrf=0.01,  # 最终学习率比例（结束时学习率 = lr0 * lrf）
        cos_lr=True,  # 是否使用余弦退火学习率策略
        #   True：学习率按余弦曲线下降，更平滑
        #   False：线性下降
        warmup_epochs=5.0,  # 预热轮数（前 5 个 epoch 学习率从 0 缓慢升到 lr0）
        #   防止初始梯度太大破坏模型
        # =========================================================================
        # 第四部分：损失函数增益 (Loss Gains)
        #   这些系数决定了模型更注重“位置准确”还是“分类准确”
        # =========================================================================
        box=7.5,  # 边框回归损失权重（越高，模型越在意框得准不准）
        cls=0.5,  # 分类损失权重（越高，模型越在意类别是否正确）
        dfl=1.5,  # 分布焦点损失权重（辅助优化框的精细度，通常保持默认）
        # =========================================================================
        # 第五部分：数据增强 (Data Augmentation)
        #   通过变换图像增加数据多样性，防止过拟合
        #   这里全部关闭以加速 CPU 训练
        # =========================================================================
        hsv_h=0,  # 色调 (Hue) 变化范围 (0-1) 可以改变
        hsv_s=0,  # 饱和度 (Saturation) 变化范围 可以改变
        hsv_v=0.4,  # 亮度 (Value) 变化范围 可以改变
        degrees=0,  # 旋转角度 (+/- deg)
        translate=0.05,  # 平移比例 (图像宽高的百分比)
        scale=0.5,  # 缩放比例 (0.5 表示图像可能缩小到 50% 或放大到 150%)
        shear=0.0,  # 错切角度
        perspective=0.0,  # 透视变换
        flipud=0.5,  # 上下翻转概率 (0.5 = 50% 概率翻转)
        fliplr=0.5,  # 左右翻转概率
        # --- 结构性增强（YOLO特色） ---
        mosaic=0,  # 马赛克增强概率 (将 4 张图拼成一张，可提升小目标检测)
        mixup=0,  # 混合增强概率 (将两张图透明度叠加)
        copy_paste=0.1,  # 复制粘贴增强概率 (主要用于分割)
        erasing=0,  # 随机擦除概率
        # =========================================================================
        # 第六部分：训练流程控制 (Flow Control)
        # =========================================================================
        patience=50,  # 早停耐心值：如果验证集精度连续 50 个 epoch 不提升，则提前停止训练
        close_mosaic=20,  # 在最后 20 个 epoch 关闭 mosaic 增强，让模型适应真实图像
        save_period=50,  # 每隔 50 个 epoch 保存一次检查点（如 epoch50.pt）
        save=True,  # 训练结束后保存模型
        save_txt=True,  # 保存推理结果为 txt 文件
        save_json=True,  # 保存详细指标为 json 文件
        save_hybrid=True,  # 保存兼容混合精度的模型（已废弃，但可以保留）
        plots=True,  # 生成混淆矩阵、PR 曲线、F1 曲线等图表
        rect=False,  # 是否使用矩形训练（False：填充为正方形；True：保持原长宽比）
    )

    # -----------------------------------------------------------------------------
    # 后处理部分（你原代码保持不变）
    # -----------------------------------------------------------------------------
    print("\n✨ 所有任务完成！")
    print(f"   📍 训练结果保存在: {results.save_dir}")


# ✅ 关键：防止多进程重复执行（必须有）
if __name__ == "__main__":
    main()
